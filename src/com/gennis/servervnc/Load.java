    public boolean start() {

        setVisible(true);
        TelaInstanceCliente.getApp().setVisible(false);

        SwingWorker swingWorker = new SwingWorker() {
            @Override
            protected Object doInBackground() throws Exception {
                init = false;
                for (int i = 5; i >= 0; i--) {

                    time.setText(String.valueOf(i));

                    Thread.sleep(1000);

                    if (i == 0) {
                        init = true;
                    }

                }

                setVisible(false);
                TelaInstanceCliente.getApp().setVisible(true);

                return null;
            }
        };

        swingWorker.execute();

        return swingWorker.isDone();
    }

    public boolean isInit() {
        return init;
    }
 

